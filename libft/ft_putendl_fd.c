#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	char	c;

	if (fd < 0 || s == NULL)
		return ;
	ft_putstr_fd(s, fd);
	c = '\n';
	write(fd, &c, 1);
}
